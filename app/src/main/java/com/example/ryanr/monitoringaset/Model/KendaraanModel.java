package com.example.ryanr.monitoringaset.Model;

public class KendaraanModel {
    String id_kendaraan, nama_kendaraan, plat, harga, nama_kondisi, nama_jenis_kendaraan, nama_rayon, keterangan, tanggal, gambar;

    public KendaraanModel() {
    }

    public String getId_kendaraan() {
        return id_kendaraan;
    }

    public void setId_kendaraan(String id_kendaraan) {
        this.id_kendaraan = id_kendaraan;
    }

    public String getNama_kendaraan() {
        return nama_kendaraan;
    }

    public void setNama_kendaraan(String nama_kendaraan) {
        this.nama_kendaraan = nama_kendaraan;
    }

    public String getPlat() {
        return plat;
    }

    public void setPlat(String plat) {
        this.plat = plat;
    }

    public String getHarga() {
        return harga;
    }

    public void setHarga(String harga) {
        this.harga = harga;
    }

    public String getNama_kondisi() {
        return nama_kondisi;
    }

    public void setNama_kondisi(String nama_kondisi) {
        this.nama_kondisi = nama_kondisi;
    }

    public String getNama_jenis_kendaraan() {
        return nama_jenis_kendaraan;
    }

    public void setNama_jenis_kendaraan(String nama_jenis_kendaraan) {
        this.nama_jenis_kendaraan = nama_jenis_kendaraan;
    }

    public String getNama_rayon() {
        return nama_rayon;
    }

    public void setNama_rayon(String nama_rayon) {
        this.nama_rayon = nama_rayon;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getGambar() {
        return gambar;
    }

    public void setGambar(String gambar) {
        this.gambar = gambar;
    }
}
